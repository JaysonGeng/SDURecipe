package Entity;

public class Tuijian {
    private int id;
    private String id_tuijian;
    private String reason;
    private String tuijian;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getId_tuijian() {
        return id_tuijian;
    }

    public void setId_tuijian(String id_tuijian) {
        this.id_tuijian = id_tuijian;
    }

    public String getReason() {
        return reason;
    }

    public void setReason(String reason) {
        this.reason = reason;
    }

    public String getTuijian() {
        return tuijian;
    }

    public void setTuijian(String tuijian) {
        this.tuijian = tuijian;
    }
}
